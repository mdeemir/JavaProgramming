package day39_Recap.cydeoTask;

public class Person {

    private String name; // koşul eklemek istediğimiz için private yaptık
    private int age; // generate kısayoldan get ve stter
    private char gender;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public char getGender() {
        return gender;
    }

    public void setGender(char gender) {
        this.gender = gender;
    }

    public Person(String name, int age, char gender) {//constructor ---> genrate constroctor
        setName(name);//koşula uygun verileri almak istideğim için setName kullandım
        setAge(age);
        setGender(gender);
    }

    public void eat(){
        System.out.println(name+" is eating");
    }

    public void drink(){
        System.out.println(name+" is drinking");
    }

    public void sleep(){
        System.out.println(name+" is sleeping");
    }


    public String toString() {
        return "Person{" +
                "name='" + name + '\'' +
                ", age=" + age +
                ", gender=" + gender +
                '}';
    }

}

